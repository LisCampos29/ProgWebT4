<?php
$i = 1;

// Bucle while que se ejecutará mientras $i sea menor o igual a 5
while ($i <= 5) {
    // Imprime el mensaje "hola: " concatenado con el valor actual de $i y añade una etiqueta <br> para realizar un salto de línea en el navegador.
    echo "hola: " . $i . "<br>";

    // Incrementa el valor de $i en 1 en cada iteración
    $i++;
}
// Fin del bucle while
?>
