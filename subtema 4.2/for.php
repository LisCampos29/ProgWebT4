<?php
for ($i = 1; $i <= 5; $i++) {
    // Inicio del bucle for. La variable $i se inicializa en 1, la condición es que $i sea menor o igual a 5 y en cada iteración $i se incrementa en 1.
    echo "Iteración: " . $i . "<br>";
    // Imprime el mensaje "Iteración: " concatenado con el valor actual de $i y añade una etiqueta <br> para realizar un salto de línea en el navegador.
}
// Fin del bucle for
