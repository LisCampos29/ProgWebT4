<?php
$opcion = 2;

// Evaluación de la variable $opcion utilizando la estructura switch
switch ($opcion) {
    case 1:
        // Si $opcion es igual a 1, se muestra el mensaje correspondiente
        echo "Seleccionaste la opción 1";
        break;
    case 2:
        // Si $opcion es igual a 2, se muestra el mensaje correspondiente
        echo "Seleccionaste la opción 2";
        break;
    case 3:
        // Si $opcion es igual a 3, se muestra el mensaje correspondiente
        echo "Seleccionaste la opción 3";
        break;
    default:
        // Si $opcion no coincide con ninguno de los casos anteriores, se muestra un mensaje de opción no válida
        echo "Opción no válida";
        break;
}
?>





